
package AceptaElReto;

import java.util.Scanner;

public class Reto1 {

    public static void main(String[] args) {
        
        Scanner leer = new Scanner(System.in);
        
        int contador;
        
        contador = leer.nextInt();
        
        for (int i = 0; i < contador; i++) {
            System.out.println("Hola mundo.");
        }
        
    }
    
}
